    @Override
    public synchronized void stateChanged(ChangeEvent e) {

        List<ProcessDescriptor> preprocessorList = detector.getDetectorDescriptor().getPreprocessors().getPreprocessorList();

        preprocessorList.clear();

        for (DetectorProcess process : selectionComponent1.getSelectedItems()) {
            if (process instanceof AbstractDetectorProcess) {
                AbstractDetectorProcess abstractDetectorProcess = (AbstractDetectorProcess) process;
                preprocessorList.add(abstractDetectorProcess.getProcessDescriptor());
            }
        }

    }
