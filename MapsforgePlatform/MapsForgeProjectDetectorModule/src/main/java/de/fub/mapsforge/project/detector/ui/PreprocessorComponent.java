    public PreprocessorComponent() {
        initComponents();
        selectionComponent1.getAllItemListTitle().setText("Registered Preprocessors"); //NO18N
        selectionComponent1.getSelectedItemListTitle().setText("Selected Preprocessors"); //NO18N
        ArrayList<FilterProcess> arrayList = new ArrayList<FilterProcess>(Lookup.getDefault().lookupResult(FilterProcess.class).allInstances());
        Collections.sort(arrayList);
        selectionComponent1.getAllItems().addAll(arrayList);
    }

    public PreprocessorComponent(Lookup lookup) {
        this();
        detector = lookup.lookup(Detector.class);
        assert detector != null;
        Collection<FilterProcess> processes = detector.getPreProcessorPipeline().getProcesses();
        selectionComponent1.getSelectedItems().addAll(processes);
        ObservableList<DetectorProcess> allItems = selectionComponent1.getAllItems();

        ArrayList<DetectorProcess> toRemoveItems = new ArrayList<DetectorProcess>();
        for (FilterProcess process : processes) {
            for (DetectorProcess detectorProcess : allItems) {
                if (process.getClass().equals(detectorProcess.getClass())) {
                    toRemoveItems.add(detectorProcess);
                }
            }
        }

        allItems.removeAll(toRemoveItems);
        selectionComponent1.getSelectedItems().addChangeListener(PreprocessorComponent.this);
    }

